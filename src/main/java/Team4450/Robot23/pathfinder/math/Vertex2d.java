package Team4450.Robot23.pathfinder.math;

public class Vertex2d extends Vertex
{

    private double x, y;

    public Vertex2d(double x, double y)
    {
        this.x = x;
        this.y = y;
    }

    public double getX()
    {
        return x;
    }

    public double getY()
    {
        return y;
    }
}
